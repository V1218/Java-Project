    private void saveDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveDetailsActionPerformed
        // TODO add your handling code here:
        // in this class where, we save the details of student data,
        String course = (String)(jComboBox1.getSelectedItem());
        String branchName = (String)(jComboBox2.getSelectedItem());
        String rollno = jTextField1.getText();
        String name = jTextField4.getText();
        String gender = (String)(jComboBox3.getSelectedItem());
        String fatherName = jTextField3.getText();
        
        try {
            Class.forName("com.mysql.cj.jdbc.Driver"); //driver class for connection with you
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms", "root", "root"); // fixed address to reach database
            
            Statement st = conn.createStatement(); // bcz mysql works only on cmds, tha's why i'm using statement class
            st.executeUpdate("Insert into student(rollno, course, branchName, name, gender, fatherName) values('"+rollno+"', '"+course+"', '"+branchName+"', '"+name+"', '"+gender+"', '"+fatherName+"')");
            
            JOptionPane.showMessageDialog(null, "Data Saved Successfully");
            setVisible(false);
            new adminHome().setVisible(true);
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_saveDetailsActionPerformed

