    private void saveResultsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveResultsActionPerformed
        // TODO add your handling code here:
        String rollno = jTextField1.getText();
        String DBMS = jTextField2.getText();
        String OS = jTextField3.getText();
        String CN = jTextField4.getText();
        String SD = jTextField5.getText();
        String OOPs = jTextField6.getText();
        
        try {
            Class.forName("com.mysql.cj.jdbc.Driver"); //driver class for connection with you
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms", "root", "root"); // fixed address to reach database
            
            Statement st = conn.createStatement(); // bcz mysql works only on cmds, tha's why i'm using statement class
            ResultSet res = st.executeQuery("select * from student where rollno = '"+rollno+"'");
            if(res.next()) {
                st.executeUpdate("Insert into result(rollno, DBMS, OS, CN, SD, OOPs) values('"+rollno+"', '"+DBMS+"', '"+OS+"', '"+CN+"', '"+SD+"', '"+OOPs+"')");
                JOptionPane.showMessageDialog(null, "Marks Saved Successfully");
                setVisible(false);
                new addResults().setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "This RollNo is not registered!");
            }
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
        
    }//GEN-LAST:event_saveResultsActionPerformed
