    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        String rollno = jTextField4.getText();
        try {
            Class.forName("com.mysql.cj.jdbc.Driver"); //driver class for connection with you
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms", "root", "root"); // fixed address to reach database
            
            Statement st = conn.createStatement(); // bcz mysql works only on cmds, tha's why i'm using statement class
            ResultSet res = st.executeQuery("select * from student INNER JOIN result where student.rollno = '"+rollno+"' and result.rollno = '"+rollno+"'");
            if(res.next()) {
                jTextField1.setText(res.getString(2));
                jTextField2.setText(res.getString(3));
                jTextField3.setText(res.getString(5));
                jTextField5.setText(res.getString(4));
                jTextField6.setText(res.getString(6));
                jTextField7.setText(res.getString(8));
                jTextField8.setText(res.getString(9));
                jTextField9.setText(res.getString(10));
                jTextField10.setText(res.getString(11));
                jTextField11.setText(res.getString(12));
                
                int s1 = Integer.parseInt(jTextField7.getText());
                int s2 = Integer.parseInt(jTextField8.getText());
                int s3 = Integer.parseInt(jTextField9.getText());
                int s4 = Integer.parseInt(jTextField10.getText());
                int s5 = Integer.parseInt(jTextField11.getText());
                
                String totalMarks = String.valueOf(s1+s2+s3+s4+s5);
                jTextField12.setText(totalMarks);
                
                if(s1 < 30 || s2 < 30 || s3 < 30 || s4 < 30 || s5 < 30) {
                    jTextField15.setText("Fail");
                } else {
                    jTextField15.setText("Pass");
                }
                 // percentage calculation
                float total = s1 + s2 + s3 + s4 + s5;
                float per = (total / 500) * 100; // Calculate percentage
                jTextField13.setText(String.valueOf(per));
                
                // Grade calculation
                // if per > 90(S), 80-90(A), 70-80(B), 60-70(C), 50-60(D), 40-50(E), 30-40(F);
                if(per > 90) {
                    jTextField14.setText("S");
                } else if(per > 80 && per < 90) {
                    jTextField14.setText("A");
                } else if(per > 70 && per < 80) {
                    jTextField14.setText("B");
                } else if(per > 60 && per < 70) {
                    jTextField14.setText("C");
                } else if(per > 50 && per < 60) {
                    jTextField14.setText("D");
                } else if(per > 40 && per < 50) {
                    jTextField14.setText("E");
                }else {
                    jTextField14.setText("F");
                }
            }
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_formComponentShown

